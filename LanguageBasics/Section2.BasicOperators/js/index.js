// Basic Operators


let year, yearJohn, yearTait;
year = 2020;
ageJohn = 28
ageTait = 32

// math operators
yearJohn = year - ageJohn;
yearTait = year - ageTait;

console.log(yearTait + ' ' + yearJohn);
console.log(year + 2);
console.log(year * 2);
console.log(year / 2);


// logical operators
let johnOlder = ageJohn < ageTait;
console.log(johnOlder);

// Typeof operator
console.log(typeof johnOlder);
console.log(typeof ageTait);
console.log(typeof 'Tait is older than John');
